<?php
$length = 10;
$width = 5;

// Calculate area
$area = $length * $width;

// Calculate perimeter
$perimeter = 2 * ($length + $width);

// Display the results
echo "For a rectangle with length $length and width $width:\n";
echo "Area: $area square units\n";
echo "Perimeter: $perimeter units\n";


?>